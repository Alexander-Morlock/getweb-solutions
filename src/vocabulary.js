  const extractedLanguage = {};
  for (const key in vocabulary) {
    extractedLanguage[key] = vocabulary[key][language];
    if (vocabulary[key].prefill) {
      extractedLanguage[`${key}-prefill`] = vocabulary[key].prefill[language]
    }
  }
  return extractedLanguage;
